class Giaodich():
    def __init__(self,_magiaodich,_ngaygiaodich,_dongia,_soluong):
        self.magiaodich = _magiaodich
        self.ngaygiaodich = _ngaygiaodich
        self.dongia = _dongia
        self.soluong = _soluong
    def GetMagiaodich(self):
        return self.magiaodich
    def GetNgaygiaodich(self):
        return self.ngaygiaodich
    def GetDongia(self):
        return self.dongia
    def GetSoluong(self):
        return self.soluong